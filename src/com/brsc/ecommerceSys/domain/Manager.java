package com.brsc.ecommerceSys.domain;

import java.sql.Date;

public class Manager extends BaseOfValue {
	
	/**管理员账号*/
	private String managerNo;
	/**管理员昵称*/
	private String managerName;
	/**管理员密码*/
	private String managerPWD;
	/**管理员真实姓名*/
	private String managerRealName;
	/**管理员性别*/
	private String managerSex;
	/**管理员出生日期*/
	private Date managerBirthday;
	/**管理员邮箱*/
	private String managerMailBox;
	/**管理员电话*/
	private String managerPhone;
	/**管理员地址*/
	private String managerAddress;
	/**管理员邮编*/
	private String managerPostCode;
	/**管理员职位 S超级管理员 M普通管理员*/
	private String managerPosition;
	
	public String getManagerNo() {
		return managerNo;
	}
	public void setManagerNo(String managerNo) {
		this.managerNo = managerNo;
	}
	public String getManagerName() {
		return managerName;
	}
	public void setManagerName(String managerName) {
		this.managerName = managerName;
	}
	public String getManagerPWD() {
		return managerPWD;
	}
	public void setManagerPWD(String managerPWD) {
		this.managerPWD = managerPWD;
	}
	public String getManagerRealName() {
		return managerRealName;
	}
	public void setManagerRealName(String managerRealName) {
		this.managerRealName = managerRealName;
	}
	public Date getManagerBirthday() {
		return managerBirthday;
	}
	public void setManagerBirthday(Date managerBirthday) {
		this.managerBirthday = managerBirthday;
	}
	
	public String getManagerMailBox() {
		return managerMailBox;
	}
	public void setManagerMailBox(String managerMailBox) {
		this.managerMailBox = managerMailBox;
	}
	public String getManagerPhone() {
		return managerPhone;
	}
	public void setManagerPhone(String managerPhone) {
		this.managerPhone = managerPhone;
	}
	public String getManagerAddress() {
		return managerAddress;
	}
	public void setManagerAddress(String managerAddress) {
		this.managerAddress = managerAddress;
	}

	public String getManagerPostCode() {
		return managerPostCode;
	}
	public void setManagerPostCode(String managerPostCode) {
		this.managerPostCode = managerPostCode;
	}
	
	public String getManagerPosition() {
		return managerPosition;
	}
	public void setManagerPosition(String managerPosition) {
		this.managerPosition = managerPosition;
	}
	public String getManagerSex() {
		return managerSex;
	}
	public void setManagerSex(String managerSex) {
		this.managerSex = managerSex;
	}
	@Override
	public String toString() {
		return "Manager [managerNo=" + managerNo + ", managerName=" + managerName + ", managerPWD=" + managerPWD
				+ ", managerRealName=" + managerRealName + ", managerSex=" + managerSex + ", managerBirthday="
				+ managerBirthday + ", managerMailBox=" + managerMailBox + ", managerPhone=" + managerPhone
				+ ", managerAddress=" + managerAddress + ", managerPostCode=" + managerPostCode + ", managerPosition="
				+ managerPosition + "]";
	}
	
}
