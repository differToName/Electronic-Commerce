package com.brsc.ecommerceSys.domain;

import java.util.Date;

public class User extends BaseOfValue {

	/**用户账号*/
	private String userNo;
	/**用户昵称*/
	private String userName;
	/**用户性别*/
	private String userSex;
	/**用户密码*/
	private String userPWD;
	/**用户真实姓名*/
	private String userRealName;
	/**用户出生日期*/
	private Date userBirthday;
	/**用户邮箱*/
	private String userMailBox;
	/**用户电话*/
	private String userPhone;
	/**用户地址*/
	private String userAddress;
	/**用户邮编*/
	private String userPostCode;
	/**用户账户状态 F冻结 A活动*/
	private String userStatus;
	
	public String getUserNo() {
		return userNo;
	}
	public void setUserNo(String userNo) {
		this.userNo = userNo;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getUserPWD() {
		return userPWD;
	}
	public void setUserPWD(String userPWD) {
		this.userPWD = userPWD;
	}
	public String getUserRealName() {
		return userRealName;
	}
	public void setUserRealName(String userRealName) {
		this.userRealName = userRealName;
	}
	public Date getUserBirthday() {
		return userBirthday;
	}
	public void setUserBirthday(Date userBirthday) {
		this.userBirthday = userBirthday;
	}
	public String getUserMailBox() {
		return userMailBox;
	}
	public void setUserMailBox(String userMailBox) {
		this.userMailBox = userMailBox;
	}
	public String getUserPhone() {
		return userPhone;
	}
	public void setUserPhone(String userPhone) {
		this.userPhone = userPhone;
	}
	public String getUserAddress() {
		return userAddress;
	}
	public void setUserAddress(String userAddress) {
		this.userAddress = userAddress;
	}
	public String getUserPostCode() {
		return userPostCode;
	}
	public void setUserPostCode(String userPostCode) {
		this.userPostCode = userPostCode;
	}
	public String getUserStatus() {
		return userStatus;
	}
	public void setUserStatus(String userStatus) {
		this.userStatus = userStatus;
	}
	public String getUserSex() {
		return userSex;
	}
	public void setUserSex(String userSex) {
		this.userSex = userSex;
	}
	@Override
	public String toString() {
		return "User [userNo=" + userNo + ", userName=" + userName + ", userSex=" + userSex + ", userPWD=" + userPWD
				+ ", userRealName=" + userRealName + ", userBirthday=" + userBirthday + ", userMailBox=" + userMailBox
				+ ", userPhone=" + userPhone + ", userAddress=" + userAddress + ", userPostCode=" + userPostCode
				+ ", userStatus=" + userStatus + "]";
	}
}
